// Takes in a string describing a license type
// and returns the corresponding MD text for that license's badge.
// If there is no license, returns an empty string.
function renderLicenseBadge(license) {
  let output = "";

  // Preempt the rest of the function execution if there is no license
  if (license === "None") {
    return output;
  }

  // Could be done with switch/case, but this is more compact
  // Generate the MD from the link and image link
  LICESNSE_TYPES.forEach((element) => {
    if (element.name === license) {
      output = `[![License: ${element.name}](${element.imgLink})](${element.licLink})`;
    }
  });

  return output;
}

// Function that returns the license link
// If there is no license, returns an empty string
function renderLicenseLink(license) {
  let output = "";

  // Special case for no license
  if (license === "None") {
    return output;
  }

  // Could be done with switch/case, but this is more compact
  // Generates the MD link
  LICESNSE_TYPES.forEach((element) => {
    if (element.name === license) {
      output = `[${element.name}](${element.licLink})`;
    }
  });

  return output;
}

// Function that returns the license section of README
// If there is no license, return an empty string
function renderLicenseSection(license) {
  // No license string
  noLicense = `## License

This code is not licensed in any way.`;

  // String for if there is a license
  yesLicense = `## License

Licensed under ${renderLicenseLink(license)}.`;

  // Return the correct string
  if (license === "None") {
    return noLicense;
  }
  return yesLicense;
}

// Generate email contact section if email was supplied
function renderEmailContact(email) {
  if (email == '') {
    return '';
  };
  emailSection = `Email: ${email}
Please reach out via provided email for any questions.`;
  return emailSection;
};

